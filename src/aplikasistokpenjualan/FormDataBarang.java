    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        btnSimpan.setEnabled(false);
        btnEdit.setEnabled(true);
        btnHapus.setEnabled(true);
        btnBatal.setEnabled(true);
        
        int i = jTable1.getSelectedRow();
        if (i == -1) {
            return;
        }
        
        String id = (String) model.getValueAt(i, 0);
        String nama = (String) model.getValueAt(i, 1);
        String jenis = (String) model.getValueAt(i, 2);
        String harga = (String) model.getValueAt(i, 3);
        String stok = (String) model.getValueAt(i, 4);
        String isi = (String) model.getValueAt(i, 5);
        
        
        txIDBarang.setText(id);
        txNamaBarang.setText(nama);
        cbJenis.setSelectedItem(jenis);
        txHarga.setText(harga);
        txStok.setText(stok);
        txIsi.setText(isi);
                                        
    }//GEN-LAST:event_jTable1MouseClicked

